// Función que recibe dos números y devuelve su media
function calcularMedia(a, b) {
  return (a + b) / 2;
}

console.log("====== Ejercicio 2: Funciones tipadas ======");

// Llamada correcta a la función
console.log("Media de 8 y 6:", calcularMedia(8, 6));
console.log("Media de 10 y 4:", calcularMedia(10, 4));


